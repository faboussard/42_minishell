#include "../../inc/libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*result;

	result = (char *)s;
	while (*result != '\0' && *result != (char)c)
		result++;
	if (*result == (char)c)
		return ((char *)result);
	return (NULL);
}
