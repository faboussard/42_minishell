#include "../../inc/libft.h"

char	*ft_strdup(const char *s)
{
	size_t len;
	char *copy;

	len = ft_strlen(s) + 1;
	copy = malloc(sizeof(char) * len);
	if (copy == NULL)
		return (NULL);
	ft_memcpy(copy, s, len);
	return (copy);
}
